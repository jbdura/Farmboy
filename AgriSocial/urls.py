from dj_rest_auth.registration.views import VerifyEmailView
from django.contrib import admin
from django.urls import path, include

from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
   openapi.Info(
      title="Snippets API",
      default_version='v1',
      description="Test description",
      terms_of_service="https://www.google.com/policies/terms/",
      contact=openapi.Contact(email="contact@snippets.local"),
      license=openapi.License(name="BSD License"),
   ),
   public=True,
   permission_classes=(permissions.AllowAny,),
)


urlpatterns = [
    path('admin/', admin.site.urls),
    path('api-auth/', include('rest_framework.urls')),

    # path("_allauth/", include("allauth.headless.urls")),
    path('accounts/', include('allauth.urls')),

    path('api/auth/', include('dj_rest_auth.urls')),
    path('api/auth/registration/', include('dj_rest_auth.registration.urls')),
    path('dj-rest-auth/account-confirm-email/', VerifyEmailView.as_view(), name='account_email_verification_sent'),

    path('api/profile/', include('userprofile.urls')),  # Include the profile app URLs
    path('api/communities/', include('community.urls')),  # Include the communities app URLs
    path('api/post/', include('post.urls')),  # Include the posts app URLs
    path('api/comment', include('comment.urls')),  # Include the comment app URLs
    path('api/likes', include('likes.urls')),  # Include the likes app URLs
    path('api/forums', include('forum.urls')),  # Include the forum app URLs

    path('swagger<format>/', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),

]
